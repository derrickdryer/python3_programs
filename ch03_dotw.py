data = int(input("Enter a number between 1 thru 7: "))

if data == 1:
    print("Monday")
elif data == 2:
    print("Tuesday")
elif data == 3:
    print("Wednesday")
elif data == 4:
    print("Thursday")
elif data == 5:
    print("Friday")
elif data == 6:
    print("Saturday")
elif data == 7:
    print("Sunday")
else:
    print("Invalid input")